let a = 10;
let b = 3;

console.log("a + b =", a + b);   // Addition
console.log("a - b =", a - b);   // Subtraction
console.log("a * b =", a * b);   // Multiplication
console.log("a / b =", a / b);   // Division
console.log("a % b =", a % b);   // Modulus (Remainder)
console.log("a ** b =", a ** b); // Exponentiation

console.log("a-- =", a--);       // Post-decrement: print first, then decrement
console.log("a-- =", a--);       // Post-decrement again
console.log("Current value of a =", a); // Check final value
console.log("--a =", --a);   //pre-decrement: decrement first,then print
console.log("++a =", ++a);  //pre-increment: increment first,then print